export function getFontFallback(fontName: string): string {
  const font = fontOptions.find((f) => f.name === fontName)
  return font?.category || "sans-serif"
}

export function buildGoogleFontsUrl(fonts: string[]): string | null {
  const unique = [...new Set(fonts.filter((f) => f && f !== "Inter"))]
  if (unique.length === 0) return null
  const families = unique.map((f) => `family=${f.replace(/ /g, "+")}:wght@300;400;500;600;700`).join("&")
  return `https://fonts.googleapis.com/css2?${families}&display=swap`
}

