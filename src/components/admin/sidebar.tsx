export function AdminSidebar({ isSuperAdmin, affiliatesEnabled = true }: { isSuperAdmin: boolean; affiliatesEnabled?: boolean }) {
  const pathname = usePathname()

  const visibleItems = navItems.filter((item) => {
    if ("superAdminOnly" in item && item.superAdminOnly && !isSuperAdmin) return false
    if ("requiresAffiliates" in item && item.requiresAffiliates && !affiliatesEnabled) return false
    return true
  })

  return (
    <aside className="fixed inset-y-0 left-0 z-30 w-64 border-r border-border bg-background">
      <div className="flex h-16 items-center border-b border-border px-6">
        <Link href="/admin" className="text-lg font-bold text-foreground">
          Admin
        </Link>
        <Link
          href="/"
          className="ml-auto text-xs text-secondary hover:text-foreground"
        >
          View Store
        </Link>
      </div>
      <nav className="space-y-1 p-4">
        {visibleItems.map((item) => {
          const isActive =
            item.href === "/admin"
              ? pathname === "/admin"
              : item.href === "/admin/settings"
                ? pathname === "/admin/settings"
                : pathname.startsWith(item.href)

          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors",
                isActive
                  ? "bg-muted text-foreground"
                  : "text-secondary hover:bg-muted hover:text-foreground"
              )}
            >
              {icons[item.icon]}
              {item.label}
            </Link>
          )
        })}
      </nav>
    </aside>
  )
}
